int check()
{
    for (int y = 0; y < 16; y++) {
        for (int x = 0; x < 92; x++) {
            if (Map[y][x] == 0) return 0;
        }
    }
    return 1;
}
 
 
void processKey()
{
    int key;
    key = _getch();
    if (key == 0 || key == 224) key = _getch();
 
    switch (key)
    {
    case UP:
        if (Map[posY - 1][posX] != 0) break;
        posY--;
        Map[posY][posX] = 1;
        break;
 
    case DOWN:
        if (Map[posY + 1][posX] != 0) break;
        posY++;
        Map[posY][posX] = 1;
        break;
 
    case LEFT:
        if (Map[posY][posX - 1] != 0) break;
        posX--;
        Map[posY][posX] = 1;
        break;
 
    case RIGHT:
        if (Map[posY][posX + 1] != 0) break;
        posX++;
        Map[posY][posX] = 1;
        break;
 
    }
}
 
void drawMap()
{
    for (int y = 0; y < 16; y++)
    {
        for (int x = 0; x < 92; x++)
        {
            switch (Map[y][x])
            {
            case 0:
                printf("  ");
                break;
 
            case 1:
                printf("♥");
                break;
 
            case 2:
                printf("□");
                break;
            }
        }
 
        printf("\n");
    }
}
